// Tipe Data Number
// Number: Balok angka (1, 2, 100, 3.14)
console.log(100);

// Tipe Data String
// String: Balok huruf yang membentuk kata atau kalimat ("Halo", "Nama Saya Budi"). Gunanya untuk teks.
console.log("Ridho Ahmad Irawan");

// Tipe Data Boolean
// Boolean: Balok saklar yang hanya punya dua sisi: true (nyala) atau false (mati). Gunanya untuk menentukan pilihan.
console.log(true);
console.log(false);